/**
 * Contains all 9 possible
 * actions that an RC Car can perform
 * 
 * @author David Widen
 * @author Jessie Young
 * @author Thomas Cheng
 *
 */
public enum Command 
{
	FORWARD,
	BACKWARD,
	LEFT,
	RIGHT,
	FORWARDLEFT,
	FORWARDRIGHT,
	BACKWARDLEFT,
	BACKWARDRIGHT,
	STOP
}